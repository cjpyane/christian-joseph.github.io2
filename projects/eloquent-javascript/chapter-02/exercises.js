////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
let board = '';
for(let row = 0; row < size; row++){
  for(let coll = 0; coll < size; coll++){
    if((row + coll) % 2 === 0){
      board += ' '
    }else{
      board += '#'
    }
  }
  board += '\n'
}
console.log(board)
}

